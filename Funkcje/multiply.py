def multiply(subject, times):
        mul = subject * times
        return mul


# poniższe linijki przetestują Twoją funkcję:
print("100 * 100 =", multiply(100, 100))  # 10000
print("2 razy 2 to", multiply(2, 2))  # 5? ;-)
print("15 * 10 =", multiply(15, 10))  # 150
print("'abc' * 10 =", multiply('abc', 10))